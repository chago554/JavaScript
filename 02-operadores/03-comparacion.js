let a = 10;
//mayor que, mayor o igual que
console.log(a > 10); // false
console.log(a >= 10); // true
 
//menor que, menor o igual que
console.log(a < 10); // false
console.log(a <= 10); // true

//compara valor sin importar el tipo de dato
console.log(a == 10); // true
console.log(a != 10); // false

//estricamente igual, compara valor y tipo de dato
console.log(a === 10); // true
console.log(a !== 10); // false
